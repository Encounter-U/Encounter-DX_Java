public class VariableDemo1
    {
        //主入口
        public static void main(String[] args)
            {
                /**
                 * 定义变量
                 * 数据类型 变量名 = 数据值;
                 * 数据类型：限定了变量能存储数据的类型
                 * int(整数) double(小数)
                 * 变量名：即存储空间的名字
                 * 作用：方便以后使用
                 * 数据值：真正存在变量中的数据
                 * 等号：赋值。把右边的数据赋值给左边的变量
                 */

                int a=10;
                System.out.println(a);

            }
    }
